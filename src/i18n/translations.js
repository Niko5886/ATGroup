let currentLang = localStorage.getItem("siteLayoutLang") || "bg";

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  if (lang !== "bg" && lang !== "en") return;
  currentLang = lang;
  localStorage.setItem("siteLayoutLang", lang);
  window.location.reload(); // Simple reload to apply changes everywhere
}

export function t(keyPath) {
  const keys = keyPath.split(".");
  let value = translations[currentLang];
  for (const key of keys) {
    value = value?.[key];
  }
  return value || keyPath;
}
